//practice of while and do while loop
#include<stdio.h>
main()
{
	int i;
	printf("\n While Loop Practice \n");
	i=11;
	while(i<=10)
	{
		printf("\n %d",i);
		i++;
	}
	//do while loop
		printf("\n Do While Loop Practice \n");
	i=1;
	do
	{
		printf("\n %d",i);
		i++;
	}while(i<=10);
	return 0;
}
